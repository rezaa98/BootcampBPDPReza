public class Latihan1_operatorAritmatika{

public static void main(String[] args) {

    int a = 2;
    int b = 1;
    int tambah, kurang, kali, bagi, sisaPembagian;

    tambah = a+b;
    kurang = a-b;
    kali = a*b;
    bagi = a/b;
    sisaPembagian = a%b;

    System.out.println(a+"+"+b+" = "+tambah);
    System.out.println(a+"-"+b+" = "+kurang);
    System.out.println(a+"*"+b+" = "+kali);
    System.out.println(a+"/"+b+" = "+bagi);
    System.out.println(a+"modulo"+b+" = "+sisaPembagian);
}
}
